const KG_TO_LBS = 2.20462;
const CM_TO_IN  = 0.393701;

/**
 * Convert a WHO table (metric) to imperial units and return as Recharts-ready objects.
 * @param {Array} table  - [[month, p5, p50, p95], ...]
 * @param {string} unit  - 'kg' or 'cm'
 * @returns {Array} [{month, p5, p50, p95}] with values in lbs or inches (2 decimal places)
 */
export function toImperial(table, unit) {
  const factor = unit === 'kg' ? KG_TO_LBS : CM_TO_IN;
  return table.map(([month, p5, p50, p95]) => ({
    month,
    p5:  Math.round(p5  * factor * 100) / 100,
    p50: Math.round(p50 * factor * 100) / 100,
    p95: Math.round(p95 * factor * 100) / 100,
  }));
}

/**
 * Pick the right WHO table based on sex and metric, returning imperial data.
 * Falls back to boys if sex is unknown/other.
 * @param {'weight'|'length'|'head'} metric
 * @param {'male'|'female'|null|undefined} sex
 * @returns {Array} [{month, p5, p50, p95}] in imperial units
 */
export function getPercentileData(metric, sex) {
  const female = sex === 'female';
  if (metric === 'weight') return toImperial(female ? WEIGHT_GIRLS : WEIGHT_BOYS, 'kg');
  if (metric === 'length') return toImperial(female ? LENGTH_GIRLS : LENGTH_BOYS, 'cm');
  if (metric === 'head')   return toImperial(female ? HEAD_GIRLS   : HEAD_BOYS,   'cm');
  return [];
}
